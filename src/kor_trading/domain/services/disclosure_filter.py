"""공시 노이즈 필터.

매매와 무관한 정기·형식 공시(임원 소유상황, 대규모기업집단현황 등)를
분류 전에 걸러낸다. LLM 호출도 줄여 속도·비용 절약.

블랙리스트 방식: 노이즈 패턴에 매칭되면 제외, 나머지는 통과(재료로 간주).
"""

from __future__ import annotations

# 제목에 이 문자열이 포함되면 노이즈로 판정 (매매 시그널 무관)
_NOISE_PATTERNS: tuple[str, ...] = (
    "소유상황보고",  # 임원ㆍ주요주주특정증권등소유상황보고서
    "대규모기업집단현황",
    "기업지배구조보고",
    "최대주주등소유주식변동",
    "주식등의대량보유상황보고",  # 5% 룰 — 대개 노이즈
    "임원ㆍ주요주주",
    "사외이사",
    "현황공시",
    "특수관계인",
    "자기주식보유현황",
)


def is_noise_disclosure(title: str) -> bool:
    """매매 무관 정기·형식 공시인지."""
    return any(pattern in title for pattern in _NOISE_PATTERNS)
